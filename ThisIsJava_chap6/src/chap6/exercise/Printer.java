package chap6.exercise;

public class Printer {
	
	public static void println(int printInt) {
		System.out.println(printInt);
	}
	public static void println(boolean printBoolean) {
		System.out.println(printBoolean);
	}
	public static void println(double printDouble) {
		System.out.println(printDouble);
	}
	public static void println(String printString) {
		System.out.println(printString);
	}
}
