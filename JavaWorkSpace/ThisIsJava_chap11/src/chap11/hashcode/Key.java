package chap11.hashcode;

public class Key {
	// hashCode()메소드를 재정의하지 않음.

	public int number;

	public Key(int number) {
		this.number = number;
	}

	// 객체가 같은지 확인 후 필드값까지 같은지 확인함.
	@Override
	public boolean equals(Object obj) {
		if (obj instanceof Key) {
			Key compareKey = (Key) obj;
			if (this.number == compareKey.number) {
				return true;
			}
		}
		return false;
	}

	@Override
	public int hashCode() {
		return number;
	}
}
